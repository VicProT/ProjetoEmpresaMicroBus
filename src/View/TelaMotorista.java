    private void EnviarMotoristaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EnviarMotoristaActionPerformed
      if(flag ==1 ){ 
       mod.setNomeMotorista(NomeMotorista.getText());
       mod.setTurno((String) TurnoMotorista.getSelectedItem());
       control.Salvar(mod);
       NomeMotorista.setEnabled(false);
       TurnoMotorista.setEnabled(false);
       CancelarMotorista.setEnabled(false);
       EnviarMotorista.setEnabled(false);
      }else{
          mod.setCodmotorista((Integer.parseInt(CodigoMotorista.getText())));
          mod.setNomeMotorista(NomeMotorista.getText());
          mod.setTurno((String) TurnoMotorista.getSelectedItem());
          control.Editar(mod);
          NomeMotorista.setEnabled(false);
          TurnoMotorista.setEnabled(false);
          CancelarMotorista.setEnabled(false);
          EnviarMotorista.setEnabled(false);
          
          CodigoMotorista.setText("");
          NomeMotorista.setText("");
          }
    }//GEN-LAST:event_EnviarMotoristaActionPerformed

