    private void PesquisarMotoristaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PesquisarMotoristaActionPerformed
        mod.setPesquisa(CampoPesquisarMotorista.getText());
        ModeloMotorista model = control.BustaMotorista(mod);//chama modelo e enviar por parametro
        CodigoMotorista.setText(String.valueOf(model.getCodmotorista()));
        NomeMotorista.setText(model.getNomeMotorista());
        TurnoMotorista.setSelectedItem(model.getTurno());
        
        EditarMotorista.setEnabled(true);
        CancelarMotorista.setEnabled(true);
    }//GEN-LAST:event_PesquisarMotoristaActionPerformed

